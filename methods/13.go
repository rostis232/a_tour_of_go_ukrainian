/*
Нульові значення інтерфейсу

Нульове значення інтерфейсу не містить ні значення, ні конкретного типу.

Виклик методу з нульового інтерфейсу є помилкою райнтайму, тому що всередині кортежу інтерфейсу немає 
типу, який би вказував на те, який конкретно метод викликати.
*/
package main

import "fmt"

type I interface {
	M()
}

func main() {
	var i I
	describe(i)
	i.M()
}

func describe(i I) {
	fmt.Printf("(%v, %T)\n", i, i)
}
