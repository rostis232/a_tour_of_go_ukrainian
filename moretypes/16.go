/*
Range (діапазон)
Форма range в циклі for дозволяє пройти по елементам слайсу чи мапи.

Під час застосування range до слайсу для кожної ітерації повертаються два значення. Перший — це індекс, а 
другий — копія елемента за цим індексом.
*/
package main

import "fmt"

var pow = []int{1, 2, 4, 8, 16, 32, 64, 128}

func main() {
	for i, v := range pow {
		fmt.Printf("2**%d = %d\n", i, v)
	}
}
