/*
Поінтер на структури
Доступ до полів структури можна отримати через поінтер структури.

Щоб отримати доступ до поля X структури, коли ми маємо покажчик структури p, ми можемо написати (*p).X.
Однак ця нотація є громіздкою, тому мова дозволяє нам натомість писати лише p.X без явного розіменування.
*/
package main

import "fmt"

type Vertex struct {
	X int
	Y int
}

func main() {
	v := Vertex{1, 2}
	p := &v
	p.X = 1e9
	fmt.Println(v)
}
