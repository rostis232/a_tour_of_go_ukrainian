/*
Слайс за замовчуванням
Під час нарізки ви можете опустити верхню або нижню межі, щоб замість них використовувати стандартні значення.

За замовчуванням для нижньої межі встановлено нуль, а для верхньої – довжина фрагмента.

Для масиву

	var a [10]int

ці вирази зрізу еквівалентні:

	a[0:10]
	a[:10]
	a[0:]
	a[:]
*/
package main

import "fmt"

func main() {
	s := []int{2, 3, 5, 7, 11, 13}

	s = s[1:4]
	fmt.Println(s)

	s = s[:2]
	fmt.Println(s)

	s = s[1:]
	fmt.Println(s)
}
